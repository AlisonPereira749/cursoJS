/**
 * Operadores de comparação
 * > Maior que
 * < Menor que
 * >= Maior que ou igual a
 * <= Menor que ou igual a
 * == Igualdade (valor)
 * === Igualdade estrita (Valor e tipo)
 * != Diferente (valor)
 * !=== Diferente estrito (valor e tipo)
 */

let num1 = 10;
let num2 = 10;
console.log(num1 > num2) //maior que
console.log(num1 < num2) //menor que
console.log(num1 >= num2) //maior que ou igual a
console.log(num1 <= num2) //menor que ou igual a
console.log(num1 == num2) // é igualdade
console.log(num1 === num2) // é igualdade estrita
console.log(num1 != num2) // é Diferente
console.log(num1 !== num2) // Diferente estrito